-- Table des stations consolidées
CREATE TABLE IF NOT EXISTS CONSOLIDATE_STATION (
    ID VARCHAR PRIMARY KEY,
    CODE VARCHAR,
    NAME VARCHAR,
    CITY_NAME VARCHAR,
    CITY_CODE VARCHAR,
    ADDRESS VARCHAR,
    LONGITUDE DOUBLE,
    LATITUDE DOUBLE,
    STATUS VARCHAR,
    CREATED_DATE DATE,
    CAPACITY INTEGER
);

-- Table des villes consolidées
CREATE TABLE IF NOT EXISTS CONSOLIDATE_CITY (
    "id" VARCHAR,
    "name" VARCHAR,
    "nb_inhabitants" INTEGER,
    "created_date" DATE,
    PRIMARY KEY ("id", "created_date")
);


-- Table des relevés de stations consolidés
CREATE TABLE IF NOT EXISTS CONSOLIDATE_STATION_STATEMENT (
    STATION_ID VARCHAR NOT NULL,
    BICYCLE_DOCKS_AVAILABLE INTEGER,
    BICYCLE_AVAILABLE INTEGER,
    LAST_STATEMENT_DATE TIMESTAMP,
    CREATED_DATE DATE,
    PRIMARY KEY (STATION_ID, CREATED_DATE)
);

